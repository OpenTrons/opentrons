import unittest

from opentrons import containers, robot
from opentrons.util import pprint


class PprintTestCase(unittest.TestCase):

    def test_pprint(self):
        robot.reset()
        containers.load('96-flat', 'B2')
        containers.load('trash-box', 'A1')
        containers.load('T25-flask', 'E3')
        containers.load('T25-flask', 'E3', 'other-flask', share=True)

        expected = \
"""+------------------------------+------------------------------+------------------------------+------------------------------+------------------------------+
|                              |                              |                              |                              |                              |
|A3                            |B3                            |C3                            |D3                            |E3                            |
|                              |                              |                              |                              |                              |
|                              |                              |                              |                              |T25-flask, other-flask        |
+------------------------------+------------------------------+------------------------------+------------------------------+------------------------------+
|                              |                              |                              |                              |                              |
|A2                            |B2                            |C2                            |D2                            |E2                            |
|                              |                              |                              |                              |                              |
|                              |96-flat                       |                              |                              |                              |
+------------------------------+------------------------------+------------------------------+------------------------------+------------------------------+
|                              |                              |                              |                              |                              |
|A1                            |B1                            |C1                            |D1                            |E1                            |
|                              |                              |                              |                              |                              |
|trash-box                     |                              |                              |                              |                              |
+------------------------------+------------------------------+------------------------------+------------------------------+------------------------------+"""  # noqa: E501

        assert pprint.deck_to_ascii(robot.deck) == expected
